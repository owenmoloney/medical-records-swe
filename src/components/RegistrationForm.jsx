// src/components/RegistrationForm.jsx
import React, { useState } from "react";
import { db, auth } from "../firebase";
import { collection, addDoc, Timestamp, doc, setDoc } from "firebase/firestore";
import { createUserWithEmailAndPassword, sendPasswordResetEmail } from "firebase/auth";

export default function RegistrationForm() {
  const [formData, setFormData] = useState({
    first_name: "",
    last_name: "",
    email: "",
    address: "",
    date_of_birth: "",
    gender: "",
    phone_number: "",
    emergency_contact_name: "",
    emergency_contact_phone: ""
  });

  const [status, setStatus] = useState("");

  const handleChange = (e) => {
    const { name, value } = e.target;
    setFormData(prev => ({ ...prev, [name]: value }));
  };

  const handleSubmit = async (e) => {
    e.preventDefault();
    setStatus("Saving...");

    try {
      // 1️⃣ Create Firebase Auth user with temporary password
      const tempPassword = Math.random().toString(36).slice(-10);
      const userCredential = await createUserWithEmailAndPassword(auth, formData.email, tempPassword);
      const user = userCredential.user;

      // 2️⃣ Send password setup email to patient
      await sendPasswordResetEmail(auth, formData.email);
      console.log("Password setup email sent to patient.");

      // 3️⃣ Generate patient_id
      const patient_id = Date.now();

      // 4️⃣ Add patient to Patients collection (unverified)
      await addDoc(collection(db, "Patients"), {
        ...formData,
        patient_id,
        user_id: user.uid,
        emailVerified: false,
        phone_number: Number(formData.phone_number),
        emergency_contact_phone: String(formData.emergency_contact_phone),
        date_of_birth: formData.date_of_birth ? Timestamp.fromDate(new Date(formData.date_of_birth)) : null,
        created_at: Timestamp.now()
      });

      // 5️⃣ Add user to Users collection for role management
      await setDoc(doc(db, "Users", user.uid), {
        uid: user.uid,
        email: formData.email,
        role: "patient",
        created_at: Timestamp.now()
      });

      setStatus("✅ Patient registered! They will receive an email to set their password.");
      setFormData({
        first_name: "",
        last_name: "",
        email: "",
        address: "",
        date_of_birth: "",
        gender: "",
        phone_number: "",
        emergency_contact_name: "",
        emergency_contact_phone: ""
      });
    } catch (error) {
      console.error("Error adding patient:", error);
      setStatus("❌ Error adding patient. Check console.");
    }
  };

  return (
    <div style={{ maxWidth: 500, margin: "2rem auto", fontFamily: "sans-serif" }}>
      <h2>Patient Registration</h2>
      <form onSubmit={handleSubmit}>
        <label>First Name:</label>
        <input name="first_name" value={formData.first_name} onChange={handleChange} required />

        <label>Last Name:</label>
        <input name="last_name" value={formData.last_name} onChange={handleChange} required />

        <label>Email:</label>
        <input type="email" name="email" value={formData.email} onChange={handleChange} required />

        <label>Address:</label>
        <input name="address" value={formData.address} onChange={handleChange} required />

        <label>Date of Birth:</label>
        <input type="date" name="date_of_birth" value={formData.date_of_birth} onChange={handleChange} required />

        <label>Gender:</label>
        <select name="gender" value={formData.gender} onChange={handleChange} required>
          <option value="">Select</option>
          <option value="male">Male</option>
          <option value="female">Female</option>
          <option value="other">Other</option>
        </select>

        <label>Phone Number:</label>
        <input name="phone_number" value={formData.phone_number} onChange={handleChange} required />

        <label>Emergency Contact Name:</label>
        <input name="emergency_contact_name" value={formData.emergency_contact_name} onChange={handleChange} required />

        <label>Emergency Contact Phone:</label>
        <input name="emergency_contact_phone" value={formData.emergency_contact_phone} onChange={handleChange} required />

        <button type="submit" style={{ marginTop: "1rem" }}>Register</button>
      </form>

      {status && <p style={{ marginTop: "1rem" }}>{status}</p>}
    </div>
  );
}

